int setup_tests(void)
{
    int security_checks;
    OSSL_PARAM params[2] = {
        OSSL_PARAM_int(OSSL_PROV_PARAM_SECURITY_CHECKS, &security_checks),
        OSSL_PARAM_END
    };
    char *config_file = NULL;
    OPTION_CHOICE o;

    while ((o = opt_next()) != OPT_EOF) {
        switch (o) {
        case OPT_CONFIG_FILE:
            config_file = opt_arg();
            break;
        default:
        case OPT_ERR:
            return 0;
        }
    }

    if (!test_get_libctx(NULL, NULL, config_file, &fips_provider, "fips"))
        return 0;

    if (!TEST_true(OSSL_PROVIDER_get_params(fips_provider, params)))
        return 0;

    if (!TEST_int_eq(security_checks, 0)) {
        TEST_info("security checks aren't disabled");
        return 0;
    }

    ADD_TEST(test_gettable_params);
    ADD_TEST(test_initial_state);
    ADD_ALL_TESTS(test_sign_and_verify, OSSL_NELEM(sign_and_verify_data));
    ADD_ALL_TESTS(test_rsa_encrypt_and_decrypt,
                  OSSL_NELEM(rsa_encrypt_and_decrypt_data));
    ADD_ALL_TESTS(test_ka, OSSL_NELEM(ka_data));
    ADD_ALL_TESTS(test_kdf, OSSL_NELEM(kdf_data));
    ADD_ALL_TESTS(test_paramgen, OSSL_NELEM(paramgen_data));
    ADD_ALL_TESTS(test_raw_sign_and_verify,
                  OSSL_NELEM(raw_sign_and_verify_data));

    return 1;
}

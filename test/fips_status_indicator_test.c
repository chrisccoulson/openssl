static int test_sign_and_verify(int n)
{
    const SIGN_AND_VERIFY_DATA *data = &sign_and_verify_data[n];
    const char *md = data->md;
    const unsigned char *key = data->key;
    size_t keylen = data->keylen;
    const OSSL_PARAM *params = data->params;
    int unapproved = data->unapproved;
    const char msg[] = "Hello World!";
    size_t msglen = sizeof(msg);
    OSSL_DECODER_CTX *dctx = NULL;
    EVP_PKEY *pkey = NULL;
    EVP_MD_CTX *mdctx = NULL;
    EVP_PKEY_CTX *pctx = NULL;
    unsigned char *sig = NULL;
    size_t siglen;
    const char *errmsg = NULL;
    int ok = 0;

    if (!TEST_ptr(dctx = OSSL_DECODER_CTX_new_for_pkey(&pkey, NULL,
                                                       NULL, NULL,
                                                       OSSL_KEYMGMT_SELECT_ALL,
                                                       NULL, NULL)))
        goto err;
    if (!TEST_true(OSSL_DECODER_from_data(dctx, &key, &keylen)))
        goto err;
    if (!TEST_ptr(mdctx = EVP_MD_CTX_new()))
        goto err;

    if (!test_unapproved(0)) {
        errmsg = "expected approved state at start of test";
        goto err;
    }

    if (!TEST_true(EVP_DigestSignInit_ex(mdctx, &pctx, md, NULL, NULL,
                                         pkey, params)))
        goto err;
    if (!TEST_true(EVP_DigestSignUpdate(mdctx, msg, msglen)))
        goto err;
    if (!TEST_true(EVP_DigestSignFinal(mdctx, NULL, &siglen)))
        goto err;
    if (!TEST_ptr(sig = OPENSSL_malloc(siglen)))
        goto err;
    if (!TEST_true(EVP_DigestSignFinal(mdctx, sig, &siglen)))
        goto err;

    if (unapproved >= 1 && !test_unapproved(1)) {
        errmsg = "expected unapproved state after sign";
        goto err;
    }
    if (!test_unapproved(0)) {
        errmsg = "expected approved state after sign";
        goto err;
    }

    if (!TEST_true(EVP_DigestVerifyInit_ex(mdctx, &pctx, md, NULL, NULL,
                                           pkey, params)))
        goto err;
    if (!TEST_true(EVP_DigestVerifyUpdate(mdctx, msg, msglen)))
        goto err;
    if (!TEST_true(EVP_DigestVerifyFinal(mdctx, sig, siglen)))
        goto err;

    if (unapproved >= 2 && !test_unapproved(1)) {
        errmsg = "expected unapproved state after verify";
        goto err;
    }
    if (!test_unapproved(0)) {
        errmsg = "expected approved state after verify";
        goto err;
    }

    ok = 1;

 err:
    OPENSSL_free(sig);
    EVP_MD_CTX_free(mdctx);
    EVP_PKEY_free(pkey);
    OSSL_DECODER_CTX_free(dctx);

    if (!ok && errmsg != NULL)
        TEST_info("%s", errmsg);

    return ok;
}

